package com.oneassist.serviceplatform.externalcontracts.dhl;

public class QualityCheck {

    private String qcCheckCode;
    private String qcCheckValue;
    private String description;
    
    public String getQcCheckCode() {
        return qcCheckCode;
    }
    
    public void setQcCheckCode(String qcCheckCode) {
        this.qcCheckCode = qcCheckCode;
    }
    
    public String getQcCheckValue() {
        return qcCheckValue;
    }
    
    public void setQcCheckValue(String qcCheckValue) {
        this.qcCheckValue = qcCheckValue;
    }
    
    public String getDescription() {
        return description;
    }
    
    public void setDescription(String description) {
        this.description = description;
    }

    
    
}
